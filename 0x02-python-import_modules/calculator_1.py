#!/usr/bin/python3

def add(a, b):
    """
    Adds two integers.

    Args:
        a: The first integer.
        b: The second integer.

    Returns:
        The sum of the two integers.
    """
    return (a + b)


def sub(a, b):
    """
    Subtracts two integers.

    Args:
        a: The first integer.
        b: The second integer.

    Returns:
        The difference between the two integers.
    """
    return (a - b)


def mul(a, b):
    """
    Multiplies two integers.

    Args:
        a: The first integer.
        b: The second integer.

    Returns:
        The product of the two integers.
    """
    return (a * b)


def div(a, b):
    """
    Divides two integers.

    Args:
        a: The dividend integer.
        b: The divisor integer.

    Returns:
        The integer division result of a by b.
    """
    return int(a / b)

